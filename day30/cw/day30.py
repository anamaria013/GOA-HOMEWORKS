# len() ფუნქცია:
# გამოიყენება სიის (ან სხვა iterable ობიექტის) ელემენტების რაოდენობის დასათვლელად.
# მაგალითად: len([1, 2, 3]) დააბრუნებს 3-ს.

# append() ფუნქცია:
# სიის ბოლოს ამატებს ახალ ელემენტს.
# მაგალითად:
# my_list = [1, 2, 3]
# my_list.append(4)  # ახლა my_list იქნება [1, 2, 3, 4]

# insert() ფუნქცია:
# სიის კონკრეტულ ინდექსზე ამატებს ახალ ელემენტს.
# მაგალითად:
# my_list = [1, 2, 3]
# my_list.insert(1, 10)  # ახლა my_list იქნება [1, 10, 2, 3]

# pop() ფუნქცია:
# სიის კონკრეტული ინდექსიდან შლის ელემენტს და აბრუნებს მას.
# თუ ინდექსი არ არის მითითებული, შლის და აბრუნებს ბოლო ელემენტს.
# მაგალითად:
# my_list = [1, 2, 3]
# removed_item = my_list.pop()  # ახლა my_list იქნება [1, 2], ხოლო removed_item = 3
# removed_item = my_list.pop(0)  # ახლა my_list იქნება [2], ხოლო removed_item = 1
# სიის შექმნა 5 სახელით
names = ["Ana", "Luka", "Nino", "Mari", "Giorgi"]

# მომხმარებლისგან სახელის შემოტანა
new_name = input("შეიყვანეთ ახალი სახელი: ")

# ახალი სახელის დამატება სიაში
names.append(new_name)

# სიის სიგრძის დაბეჭდვა
print("სიის სიგრძე არის:", len(names))

# განახლებული სიის დაბეჭდვა
print("განახლებული სია:", names)
# ხილის სიის შექმნა
fruits = ["Melon", "Orange", "Banana", "Watermelon", "Kiwi"]

# ბოლო ელემენტის ამოღება
fruits.pop()

# მესამე ინდექსზე ახალი ელემენტის ჩასმა
fruits.insert(3, "Pomegranate")

# განახლებული სიის დაბეჭდვა
print("განახლებული ხილის სია:", fruits)